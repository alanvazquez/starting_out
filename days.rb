# Case statement in Ruby
# Taking Input in Rubuy
day = gets.chomp
# Applying case statement
case day
when "Tuesday"
  puts 'Eat Rajma Chawal'
when "Wednesday"
  puts 'Eat Butter chicken'
when "Thursday"
  puts 'Eat Chole Chawal'
 when "Friday"
  puts 'Eat Egg curry'
 when "Saturday"
  puts 'Eat Butter Paneer'
else
  puts "This is Fast day so eat fruits"
end
